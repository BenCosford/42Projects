int ft_toupper(int  c)

{
    unsigned char   *str;

    c = 97;
    if ((c > 96) && (c < 123))
    {
        str[c] - 32;
        c++;
        return (str);
    }
    else if ((c > 64) && (c < 91))
    return (str);
}
