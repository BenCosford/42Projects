int ft_tolower(int  c)

{
    unsigned char   *str;

    c = 65;
    if ((c > 64) && (c < 91))
    {
        str[c] + 32;
        c++;
        return (str);
    }
    else if ((c > 96) && (c < 123))
    return (str);
}
